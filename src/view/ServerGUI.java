    public void appendClient(ArrayList c) {
        lstClient.removeAll();
        DefaultListModel model = new DefaultListModel();

        for (int i = 0; i < c.size(); i++) {

            model.addElement(c.get(i));
        }
        lstClient.setModel(model);
    }

    public void appendRoom(String str) {
        txtChatRoom.append(str);
        txtChatRoom.setCaretPosition(txtChatRoom.getText().length() - 1);
    }

    public void appendEvent(String str) {
        txtEventLog.append(str);
        txtEventLog.setCaretPosition(txtEventLog.getText().length() - 1);
    }

