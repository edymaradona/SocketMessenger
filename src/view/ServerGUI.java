    private void btnStartStopActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStartStopActionPerformed
        if (server != null) {
            server.stop();
            txtListen.setText("");
            server = null;
            txtPortNumber.setEditable(true);
            btnStartStop.setText("Start");
            return;
        }
        int port;
        try {
            port = Integer.parseInt(txtPortNumber.getText().trim());
        } catch (Exception er) {
            appendEvent("Invalid port number");
            return;
        }
        server = new ServerClass(port, this);
        new ServerGUI.ServerRunning().start();
        btnStartStop.setText("Stop");
        txtPortNumber.setEditable(false);

        try {
            String hostName = InetAddress.getLocalHost().getHostName();
            //System.out.println("HostName = " + hostName);
            //System.out.println("HostAddressLocal = "
            //      + InetAddress.getLocalHost().getHostAddress());
            InetAddress[] inetAddresses = InetAddress.getAllByName(hostName);

            for (InetAddress inetAddress : inetAddresses) {
                if (!inetAddress.getHostAddress().contains(":")) {

                    txtListen.append(inetAddress.getHostAddress() + ":" + port + "\n");

                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }


    }//GEN-LAST:event_btnStartStopActionPerformed

